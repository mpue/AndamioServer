package org.pmedv.actions;

/**
 * This class represents the actions, a form can have. 
 * For example : save, mainmenu, reload and so on
 *	
 *	This class is used to build a flexible buttonbar for every form
 *
 * @author Matthias Pueski
 *
 */
@Deprecated
public class FormAction {
	
	private String key;
	private String target;

	
	public FormAction(String key, String target) {
		this.key = key;
		this.target = target;
	}
	
	/**
	 * @return the key
	 */
	public String getKey() {
		return key;
	}
	/**
	 * @param key the key to set
	 */
	public void setKey(String key) {
		this.key = key;
	}
	/**
	 * @return the target
	 */
	public String getTarget() {
		return target;
	}
	/**
	 * @param target the target to set
	 */
	public void setTarget(String target) {
		this.target = target;
	}

}
